#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	dstLen;
	size_t srcLen;

	dstLen = ft_strlen(dst);
	srcLen = ft_strlen(src);

	if (dstLen <= size)
		return (srcLen + size);
	i = 0;
	while (i < size - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] ='\0';
	return (srcLen);
}
